    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
     try {

        double rata = (Double.parseDouble(txtn1.getText()) +
                       Double.parseDouble(txtn2.getText())) / 2;

        String sql = "INSERT INTO mahasiswa VALUES (?,?,?,?,?,?)";

        PreparedStatement ps = conn.prepareStatement(sql);

        ps.setString(1, txtnim.getText());
        ps.setString(2, txtnama.getText());
        ps.setString(3, txtmk.getText());
        ps.setDouble(4, Double.parseDouble(txtn1.getText()));
        ps.setDouble(5, Double.parseDouble(txtn2.getText()));
        ps.setDouble(6, rata);

        ps.executeUpdate();

        JOptionPane.showMessageDialog(null, "Data berhasil disimpan");

        tampilData();

    } catch (Exception e) {

        JOptionPane.showMessageDialog(null, e);

    }


    }//GEN-LAST:event_btnSimpanActionPerformed

