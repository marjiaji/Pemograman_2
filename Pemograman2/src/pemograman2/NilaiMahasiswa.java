private void tampilData() {

    model = new DefaultTableModel();

    model.addColumn("NIM");
    model.addColumn("Nama");
    model.addColumn("Mata Kuliah");
    model.addColumn("Nilai1");
    model.addColumn("Nilai2");
    model.addColumn("Rata-rata");

    try {
        st = conn.createStatement();
        rs = st.executeQuery("SELECT * FROM mahasiswa");

        while (rs.next()) {
            model.addRow(new Object[]{
                rs.getString("nim"),
                rs.getString("nama"),
                rs.getString("matakuliah"),
                rs.getString("nilai1"),
                rs.getString("nilai2"),
                rs.getString("rata")
            });
        }

        jTable2.setModel(model);

    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, e);
    }
}
